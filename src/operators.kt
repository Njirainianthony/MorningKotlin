fun main() {

    //ARITHMETIC OPERATORS
    var x=10
    var y:Double=98.0

    println(x+y)
    println(x-y)
    println(x*y)
    println(x/y)
    println(y%x) //MODULUS - RETURNS THE REMAINDER


    //COMPARISON OPERATORS
    var a=45
    var b=23
    println(a<b)
    println(a>b)
    println(a==b) //EQUAL TO
    println(a<=b)
    println(a>=b)
    println(a!=b) //NOT EQUAL TO


    //ASSIGNMENT OPERATORS
    var z=56
    println(z)

    //ADDITION OPERATORS
    var num=10
    num+=2
    println(num)


    //LOGIC OPERATORS - && , ||, !

    println(67>12 && 76<6)
    println(67>12 || 76<6)
    println(!(67>12 || 76<6))















}